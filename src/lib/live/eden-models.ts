export interface EdenModel {
  /** Provider key used in Eden AI API, e.g. "openai" */
  provider: string;
  /** Model name within the provider, passed in settings.{provider}.model */
  modelId: string | null;
  displayName: string;
  providerLabel: string;
  /** USD per 1M input tokens */
  inputCostPer1M: number;
  /** USD per 1M output tokens */
  outputCostPer1M: number;
  contextWindow: number;
  tier: "free" | "cheap" | "mid" | "premium";
}

/** Generation parameters for LLM calls */
export interface GenerationParams {
  temperature?: number; // 0.0 - 2.0, default ~0.7
  top_p?: number; // 0.0 - 1.0, default ~1.0
  top_k?: number; // 1+, default ~50
  max_tokens?: number; // max output length
  frequency_penalty?: number; // -2.0 to 2.0
  presence_penalty?: number; // -2.0 to 2.0
}

/** Key used in Eden AI API requests: "provider" or "provider/modelId" */
export function edenProviderKey(model: EdenModel): string {
  return model.provider;
}

/** Settings block for a specific model selection */
export function edenSettings(models: EdenModel[]): Record<string, string> {
  const hasModelId = (model: EdenModel): model is EdenModel & { modelId: string } =>
    model.modelId !== null;

  return Object.fromEntries(models.filter(hasModelId).map((m) => [m.provider, m.modelId]));
}

