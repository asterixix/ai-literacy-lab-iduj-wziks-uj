export function groupByProvider(models: EdenModel[]): Record<string, EdenModel[]> {
  return models.reduce<Record<string, EdenModel[]>>((acc, m) => {
    if (!acc[m.provider]) acc[m.provider] = [];
    acc[m.provider].push(m);
    return acc;
  }, {});
}

